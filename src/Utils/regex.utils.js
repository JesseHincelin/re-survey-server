export const REGEX = {
  email: /^[a-z0-9._-]+@[a-z0-9._-]+\.[a-z]{2,6}$/,
  phone:
    /((?:\+|00)[17](?: |-)?|(?:\+|00)[1-9]\d{0,2}(?: |-)?|(?:\+|00)1-\d{3}(?: |-)?)?(0\d|\([0-9]{3}\)|[1-9]{0,3})(?:((?: |-)[0-9]{2}){4}|((?:[0-9]{2}){4})|((?: |-)[0-9]{3}(?: |-)[0-9]{4})|([0-9]{7}))/,
  password:
    /^(?:(?=.*[a-z])(?:(?=.*[A-Z])(?=.*[\d])(?=.*[\W]))|(?=.*\W)(?=.*[a-z])(?=.*[A-Z])(?=.*\d)).{8,}$/,
  // date: /^(?:(?:31(\/|-|\.)(?:0?[13578]|1[02]))\1|(?:(?:29|30)(\/|-|\.)(?:0?[13-9]|1[0-2])\2))(?:(?:1[6-9]|[2-9]\d)?\d{2})$|^(?:29(\/|-|\.)0?2\3(?:(?:(?:1[6-9]|[2-9]\d)?(?:0[48]|[2468][048]|[13579][26])|(?:(?:16|[2468][048]|[3579][26])00))))$|^(?:0?[1-9]|1\d|2[0-8])(\/|-|\.)(?:(?:0?[1-9])|(?:1[0-2]))\4(?:(?:1[6-9]|[2-9]\d)?\d{2})$/,
  date: /^\d{4}\-(0[1-9]|1[012])\-(0[1-9]|[12][0-9]|3[01])$/,
};

export const regexIsOk = (regex, value) => regex.test(value);
export const emailIsValid = (email) => regexIsOk(REGEX.email, email);
export const dateIsValid = (date) => regexIsOk(REGEX.date, date);
export const passwordIsValid = (password) => regexIsOk(REGEX.password, password);
